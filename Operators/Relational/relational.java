package Operators.Relational;

public class relational {
    public static void main(String[] args){
        int x , y;
        x = 10;
        y = 20;
        boolean compare = x > y;
        System.out.println("Compare:"+compare);
        compare = x < y;
        System.out.println("Compare:"+compare);
        compare = x != y;
        System.out.println("Compare:"+compare);
        compare = x >= y;
        System.out.println("Compare:"+compare);
        compare = x <= y;
        System.out.println("Compare:"+compare);
        compare = x == y;
        System.out.println("Compare:"+compare);
    }
}
